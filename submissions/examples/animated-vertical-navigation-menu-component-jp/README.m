# Animated Vertical Navigation Menu Component

## Overview

A modern CSS-only vertical navigation menu featuring smooth slide animations, hover transitions, active menu indicators, and a sidebar-style layout.

## Features

- Smooth sidebar entrance animation
- Hover slide effects on navigation items
- Active menu highlighting
- Modern dashboard-style design
- Responsive and lightweight layout
- Pure HTML and CSS implementation

## Files

- demo.html – Demonstrates the vertical navigation menu
- style.css – Contains menu styling and animations
- README.md – Provides documentation and usage instructions

## Usage

Open demo.html in a modern browser to view the animated vertical navigation menu.

Hover over navigation items to experience the smooth slide interaction effects.